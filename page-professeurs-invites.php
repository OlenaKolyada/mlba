                            <p>Yumi Aizawa, née à Matsumoto (Japon), a débuté le ballet à la Hakucho Ballet Academy avant de poursuivre sa formation à la Hamburg Ballet School en Allemagne, où elle a également participé à des productions. À 17 ans, elle rejoint le Ballet de Dresde, interprétant des œuvres classiques et néoclassiques, puis intègre le Ballet de l’Opéra National de Bordeaux. Promue soliste en 2009, puis première soliste en 2012, elle y tient des rôles principaux dans des ballets classiques ainsi que des créations de chorégraphes tels qu’I. Galili, J. Kylian, W. Forsythe.</p>
