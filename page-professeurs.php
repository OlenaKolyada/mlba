                            explorant aussi bien le répertoire classique que contemporain. En 2018, il devient Assistant Maître de Ballet
                            sur une grande production, puis met fin à sa carrière sur scène pour se consacrer à l’enseignement.</p>
                        <p>Reconnu pour son parcours, il obtient son diplôme d’État et enseigne la danse classique selon la méthode Vaganova et l’école française.
